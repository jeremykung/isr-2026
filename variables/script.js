// Variables
// variables are like containers that hold something
// below the x container holds tea

var x = "tea"   // defining variable
x = "phone"     // mutating variable
x = "anars"
console.log(x)

// 2018 convention changed from using "var" to "const/let"

// const variables CANNOT change
const y = "marker"
// y = "mark"
console.log(y)

// let variables CAN change
let z = "computer"
console.log(z)

// PRACTICE
let mainCharacter = "Natalia"     // String variables
let age = 12                    // Integer variables

console.log(mainCharacter + " is " + age)